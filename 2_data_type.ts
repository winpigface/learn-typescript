console.log("Hello World");
console.log("1");
console.log("1 + 1");
console.log("Single quote");
console.log("Double quote 'This's single quote'");
console.log(`Back quote`);
console.log("Message 1" + "Message 2");
console.log("100" + "5" + "6" + "5");
console.log(100 + "5");
// Boolean
console.log(true);
console.log(false);
console.log(1 > 0);
console.log(1 < 0);
console.log(0 == 0);
console.log(1 == '1')
console.log(1 == "1");
